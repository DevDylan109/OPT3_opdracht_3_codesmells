package com.example.opt3_opdracht_2;

public class Klant {
    private String naam;
    private String achternaam;

    public Klant(String naam, String achternaam) {
        this.naam = naam;
        this.achternaam = achternaam;
    }

    public String getNaam() {
        return naam;
    }


    public String getAchternaam() {
        return achternaam;
    }

}
